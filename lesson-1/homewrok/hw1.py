# 1. Square: find perimeter and area
side = float(input("Enter side of the square: "))
perimeter = 4 * side
area = side ** 2
print(f"Perimeter: {perimeter}")
print(f"Area: {area}")

# 2. Circle: find circumference
diameter = float(input("Enter diameter of the circle: "))
circumference = 3.14 * diameter
print(f"Circumference: {circumference}")

# 3. Mean of two numbers
a = float(input("Enter first number (a): "))
b = float(input("Enter second number (b): "))
mean = (a + b) / 2
print(f"Mean: {mean}")

# 4. Sum, product, and squares
sum_ab = a + b
product_ab = a * b
square_a = a ** 2
square_b = b ** 2
print(f"Sum: {sum_ab}")
print(f"Product: {product_ab}")
print(f"Square of a: {square_a}")
print(f"Square of b: {square_b}")

